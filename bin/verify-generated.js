#!/usr/bin/env node

import {spawnSync} from 'node:child_process'
import path from 'node:path'
import {fileURLToPath} from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.resolve(__dirname, '..')

const graphqlFamily = 'graphql'
const oclifFamilies = ['manifests', 'readme', 'code-documentation', 'shopify-dev-docs']
const allFamilies = [graphqlFamily, ...oclifFamilies]
const aliases = new Map([
  ['all', allFamilies],
  ['oclif', oclifFamilies],
  ['docs', ['readme', 'code-documentation', 'shopify-dev-docs']],
])

const usage = `Usage: pnpm verify:generated [options]

Runs the generated-file freshness checks enforced by tests-pr.yml.

Options:
  --only <families>   Comma-separated families: all, graphql, oclif, manifests,
                      readme, code-documentation, shopify-dev-docs, docs
  --dry-run           Print the commands and git checks without running them
  --skip-build        Skip the OCLIF/docs pre-build step
  -h, --help          Show this help message
`

const options = parseArguments(process.argv.slice(2))

if (options.help) {
  console.log(usage)
  process.exit(0)
}

const selectedFamilies = expandFamilies(options.only)
const initialSnapshots = options.dryRun ? new Map() : initialSnapshotsFor(selectedFamilies)

if (selectedFamilies.has(graphqlFamily)) {
  run('pnpm', ['graphql-codegen:get-graphql-schemas'])
  run('pnpm', ['graphql-codegen'])
  checkUnchanged([], 'Run pnpm graphql-codegen when queries are changed, committing any fixes and generated files.')
}

if (oclifFamilies.some((family) => selectedFamilies.has(family))) {
  if (!options.skipBuild) {
    run('pnpm', ['build', '--output-style=stream'])
  }

  if (selectedFamilies.has('manifests')) {
    run('pnpm', ['refresh-manifests'])
    checkUnchanged(['**/oclif.manifest.json'], 'Run pnpm refresh-manifests before pushing new commands or flags.')
  }

  if (selectedFamilies.has('readme')) {
    run('pnpm', ['refresh-readme'])
    checkUnchanged(['packages/cli/README.md'], 'Run pnpm refresh-readme before pushing new commands or flags.')
  }

  if (selectedFamilies.has('code-documentation')) {
    run('pnpm', ['refresh-code-documentation'])
    checkUnchanged([], 'Run pnpm refresh-code-documentation when you update functions with autogenerated examples.')
  }

  if (selectedFamilies.has('shopify-dev-docs')) {
    run('pnpm', ['build-dev-docs'])
    checkUnchanged(
      ['docs-shopify.dev/generated/*.json'],
      'Run pnpm build-dev-docs before pushing new commands or flags.',
    )
  }
}

if (options.dryRun) {
  console.log('\nDry run complete.')
} else {
  console.log('\nGenerated files are fresh.')
}

function parseArguments(args) {
  const parsed = {
    dryRun: false,
    help: false,
    only: ['all'],
    skipBuild: false,
  }

  for (let index = 0; index < args.length; index++) {
    const arg = args[index]

    if (arg === '--dry-run') {
      parsed.dryRun = true
    } else if (arg === '--skip-build') {
      parsed.skipBuild = true
    } else if (arg === '--help' || arg === '-h') {
      parsed.help = true
    } else if (arg === '--only') {
      const value = args[index + 1]
      if (!value) {
        failWithUsage('--only requires a comma-separated value.')
      }
      parsed.only = value.split(',')
      index++
    } else if (arg.startsWith('--only=')) {
      parsed.only = arg.slice('--only='.length).split(',')
    } else {
      failWithUsage(`Unknown option: ${arg}`)
    }
  }

  return parsed
}

function expandFamilies(values) {
  const selected = new Set()

  for (const rawValue of values) {
    const value = rawValue.trim()

    if (value === '') {
      continue
    }

    if (aliases.has(value)) {
      for (const family of aliases.get(value)) {
        selected.add(family)
      }
    } else if (allFamilies.includes(value)) {
      selected.add(value)
    } else {
      failWithUsage(`Unknown generated freshness family: ${value}`)
    }
  }

  if (selected.size === 0) {
    failWithUsage('Select at least one generated freshness family.')
  }

  return selected
}

function initialSnapshotsFor(selectedFamilies) {
  const pathspecsToSnapshot = []

  if (selectedFamilies.has(graphqlFamily) || selectedFamilies.has('code-documentation')) {
    pathspecsToSnapshot.push([])
  }

  if (selectedFamilies.has('manifests')) {
    pathspecsToSnapshot.push(['**/oclif.manifest.json'])
  }

  if (selectedFamilies.has('readme')) {
    pathspecsToSnapshot.push(['packages/cli/README.md'])
  }

  if (selectedFamilies.has('shopify-dev-docs')) {
    pathspecsToSnapshot.push(['docs-shopify.dev/generated/*.json'])
  }

  return new Map(pathspecsToSnapshot.map((pathspecs) => [pathspecKey(pathspecs), gitSnapshot(pathspecs)]))
}

function run(command, args) {
  const printableCommand = [command, ...args].join(' ')

  if (options.dryRun) {
    console.log(`$ ${printableCommand}`)
    return
  }

  console.log(`\n$ ${printableCommand}`)
  const result = spawnSync(command, args, {cwd: root, stdio: 'inherit'})

  if (result.status !== 0) {
    process.exit(result.status ?? 1)
  }
}

function checkUnchanged(pathspecs, message) {
  const printablePathspecs = pathspecs.length === 0 ? '' : ` -- ${pathspecs.join(' ')}`

  if (options.dryRun) {
    console.log(`$ git status --porcelain${printablePathspecs}`)
    console.log(`$ git diff${printablePathspecs}`)
    return
  }

  const baseline = initialSnapshots.get(pathspecKey(pathspecs))
  const snapshot = gitSnapshot(pathspecs)

  if (snapshot.status === baseline.status && snapshot.diff === baseline.diff) {
    return
  }

  console.error(`\n${message}

Changed files:

${formatStatusChange(baseline.status, snapshot.status)}`)
  printDiff(pathspecs)
  process.exit(1)
}

function gitSnapshot(pathspecs) {
  return {
    diff: gitDiff(pathspecs),
    status: gitStatus(pathspecs),
  }
}

function gitStatus(pathspecs) {
  const result = spawnSync('git', ['status', '--porcelain', ...gitPathspecArgs(pathspecs)], {
    cwd: root,
    encoding: 'utf8',
  })

  if (result.status !== 0) {
    process.stderr.write(result.stderr)
    process.exit(result.status ?? 1)
  }

  return result.stdout.trim()
}

function printDiff(pathspecs) {
  const diff = gitDiff(pathspecs)

  if (diff.trim() !== '') {
    console.error(`\nDiff:\n\n${diff}`)
  }
}

function gitDiff(pathspecs) {
  const result = spawnSync('git', ['diff', ...gitPathspecArgs(pathspecs)], {
    cwd: root,
    encoding: 'utf8',
  })

  if (result.status !== 0) {
    process.stderr.write(result.stderr)
    process.exit(result.status ?? 1)
  }

  return result.stdout
}

function gitPathspecArgs(pathspecs) {
  return pathspecs.length === 0 ? [] : ['--', ...pathspecs]
}

function pathspecKey(pathspecs) {
  return pathspecs.join('\0')
}

function formatStatusChange(before, after) {
  if (before === '') {
    return after
  }

  return `Before:
${before}

After:
${after}`
}

function failWithUsage(message) {
  console.error(`${message}\n\n${usage}`)
  process.exit(1)
}
