#!/usr/bin/env node

import {spawnSync} from 'node:child_process'

const dryRun = process.argv.includes('--dry-run')
const help = process.argv.includes('--help') || process.argv.includes('-h')
const unknownOption = process.argv.slice(2).find((arg) => !['--dry-run', '--help', '-h'].includes(arg))

if (help) {
  console.log(`Usage: pnpm verify:generated [--dry-run]

Runs the generated-file freshness checks from .github/workflows/tests-pr.yml.`)
  process.exit(0)
}

if (unknownOption) {
  console.error(`Unknown option: ${unknownOption}`)
  process.exit(1)
}

checkClean('', 'Commit or stash local changes before running generated freshness checks')

run('pnpm graphql-codegen:get-graphql-schemas')
run('pnpm graphql-codegen')
run('git diff')
checkClean('', 'Run pnpm graphql-codegen when queries are changed, committing any fixes and generated files')

run('pnpm build --output-style=stream')
run('pnpm refresh-manifests')
checkClean('**/oclif.manifest.json', 'Run pnpm refresh-manifests before pushing new commands or flags')

run('pnpm refresh-readme')
checkClean('packages/cli/README.md', 'Run pnpm refresh-readme before pushing new commands or flags')

run('pnpm refresh-code-documentation')
checkClean('', 'Run pnpm refresh-code-documentation when you update functions with autogenerated examples')

run('pnpm build-dev-docs')
checkClean('docs-shopify.dev/generated/*.json', 'Run pnpm build-dev-docs before pushing new commands or flags')

console.log('Generated files are fresh.')

function run(command) {
  if (dryRun) {
    console.log(`$ ${command}`)
    return
  }

  console.log(`\n$ ${command}`)
  const result = spawnSync(command, {shell: true, stdio: 'inherit'})
  if (result.status !== 0) process.exit(result.status ?? 1)
}

function checkClean(pathspec, message) {
  const command = pathspec ? `git status --porcelain "${pathspec}"` : 'git status --porcelain'
  const status = output(command)
  if (status === '') return

  console.error(`${message}.`)
  console.error('\nChanged files:\n')
  console.error(status)
  process.exit(1)
}

function output(command) {
  if (dryRun) {
    console.log(`$ ${command}`)
    return ''
  }

  const result = spawnSync(command, {shell: true, encoding: 'utf8'})
  if (result.status !== 0) {
    process.stderr.write(result.stderr)
    process.exit(result.status ?? 1)
  }
  return result.stdout.trim()
}
