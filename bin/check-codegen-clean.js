// Asserts that a codegen step left the working tree clean for its owned paths.
// Single source of truth for the codegen freshness checks run both locally
// (via `pnpm codegen:check*`) and in CI (tests-pr.yml). Each key mirrors one
// regenerate-then-verify step; the message tells the developer how to fix it.
import {execFileSync} from 'node:child_process'

const CHECKS = {
  graphql: {
    pathspec: [],
    message: 'Run pnpm codegen (or pnpm graphql-codegen) when queries change, then commit the generated files.',
  },
  'oclif:manifests': {
    pathspec: ['**/oclif.manifest.json'],
    message: 'Run pnpm codegen (or pnpm refresh-manifests) before pushing new commands or flags.',
  },
  'oclif:readme': {
    pathspec: ['packages/cli/README.md'],
    message: 'Run pnpm codegen (or pnpm refresh-readme) before pushing new commands or flags.',
  },
  'oclif:code-docs': {
    pathspec: [],
    message: 'Run pnpm codegen (or pnpm refresh-code-documentation) when you update functions with autogenerated examples.',
  },
  'oclif:dev-docs': {
    pathspec: ['docs-shopify.dev/generated/*.json'],
    message: 'Run pnpm codegen (or pnpm build-dev-docs) before pushing new commands or flags.',
  },
}

const key = process.argv[2]
const check = CHECKS[key]
if (!check) {
  console.error(`Unknown codegen check "${key}". Known checks: ${Object.keys(CHECKS).join(', ')}`)
  process.exit(2)
}

const porcelain = execFileSync('git', ['status', '--porcelain', ...check.pathspec], {encoding: 'utf8'})
if (porcelain.trim() === '') {
  process.exit(0)
}

const diff = execFileSync('git', ['diff', ...check.pathspec], {encoding: 'utf8'})
console.error(check.message)
console.error('\nDiff here:\n')
console.error(diff)
process.exit(1)
